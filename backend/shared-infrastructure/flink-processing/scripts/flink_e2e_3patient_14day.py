    total_m9 = len(rajesh_m9) + len(outputs["M9"].get("priya", [])) + len(outputs["M9"].get("amit", []))
    results.append(AssertionResult("S26", "M9 produces engagement signals",
        total_m9 > 0, f"total={total_m9} [timer-dependent: may be 0 in batch E2E]"))

    rajesh_m10 = outputs["M10"].get("rajesh", [])
    total_m10 = len(rajesh_m10) + len(outputs["M10"].get("priya", [])) + len(outputs["M10"].get("amit", []))
    results.append(AssertionResult("S27", "M10 produces meal responses >= 5 days",
        total_m10 >= 5, f"total={total_m10} [timer-dependent: may be 0 in batch E2E]"))

    return results


