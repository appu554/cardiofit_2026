    rajesh_confidence = max(
        (m.get("data_completeness_at_change") or m.get("confidenceScore") or 0.0
         for m in rajesh_m13), default=0.0)
    results.append(AssertionResult("H13", "M13 Rajesh confidence_score >= 0.5",
        rajesh_confidence >= 0.5,
        f"max_confidence={rajesh_confidence:.3f} [batch: M9/M10 timer-dependent → 0.5 expected]"))

