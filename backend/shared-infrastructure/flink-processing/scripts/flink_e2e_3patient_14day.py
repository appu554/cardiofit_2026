class AssertionResult:
    def __init__(self, id, description, passed, detail=""):
        self.id = id
        self.description = description
        self.passed = passed
        self.detail = detail

    def __str__(self):
        status = "PASS" if self.passed else "FAIL"
        d = f" — {self.detail}" if self.detail else ""
        return f"  [{status}] {self.id}: {self.description}{d}"


def run_assertions(outputs, patient_ids):
    """Run all 25 playbook assertions. Returns list of AssertionResult."""
    results = []

    rajesh = patient_ids.get("rajesh", "")
    priya = patient_ids.get("priya", "")
    amit = patient_ids.get("amit", "")

