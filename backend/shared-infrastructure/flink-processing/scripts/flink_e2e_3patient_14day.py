def print_module_summary(outputs, patient_names):
    """Print per-module, per-patient output counts."""
    print(f"\n{'═' * 72}")
    print(f"  MODULE OUTPUT SUMMARY")
    print(f"{'═' * 72}")

    header = f"  {'Module':6s} {'Topic':42s}"
    for name in patient_names:
        header += f" {name:>8s}"
    header += f" {'Total':>6s}  Trigger"
    print(header)
    print(f"  {'-' * 70}")

    triggers = {
        "M7": "immediate", "M8": "immediate", "M9": "timer 23:59 UTC",
        "M10": "timer meal+3h", "M10b": "weekly Mon 00:00",
        "M11": "timer act+2h", "M11b": "weekly Mon 00:00",
        "M12": "immediate", "M12b": "on WINDOW_CLOSED", "M13": "immediate",
    }

    total_all = 0
    for module, topic in OUTPUT_TOPICS.items():
        row = f"  {module:6s} {topic:42s}"
        mod_total = 0
        for name in patient_names:
            count = len(outputs[module].get(name, []))
            mod_total += count
            marker = f"{count:>8d}" if count > 0 else f"{'0':>8s}"
            row += marker
        row += f" {mod_total:>6d}  {triggers.get(module, '')}"
        icon = "✓" if mod_total > 0 else "○"
        print(f"  {icon}{row[2:]}")
        total_all += mod_total

    print(f"  {'-' * 70}")
    print(f"  Total outputs across all modules: {total_all}")


def print_m7_detail(outputs, patient_names):
    """Print M7 detail for each patient's last reading."""
    print(f"\n  ── M7 BP Variability — Final Readings ──")
    for name in patient_names:
        msgs = outputs["M7"].get(name, [])
        if not msgs:
            print(f"  {name.upper():8s}: No M7 output")
            continue
        last = msgs[-1]
        arv = last.get("arv_sbp_7d")
        var_class = last.get("variability_classification_7d", "?")
        crisis = last.get("crisis_flag", False)
        surge = last.get("morning_surge_today")
        dip = last.get("dip_classification", "?")
        masked = last.get("masked_htn_suspected", False)
        bp_status = last.get("bp_control_status", "?")
        depth = last.get("context_depth", "?")
        sbp_avg = last.get("sbp_7d_avg")

        arv_s = f"{arv:.1f}" if arv is not None else "null"
        surge_s = f"{surge:.0f}" if surge is not None else "null"
        sbp_s = f"{sbp_avg:.0f}" if sbp_avg is not None else "null"

        print(f"  {name.upper():8s}: readings={len(msgs):2d}  ARV={arv_s:>6s}  "
              f"class={var_class:20s}  sbp_avg={sbp_s}")
        print(f"           crisis={crisis}  surge={surge_s}  dip={dip}  "
              f"masked={masked}  status={bp_status}  depth={depth}")


def print_m8_detail(outputs, patient_names):
    """Print M8 alert details per patient."""
    print(f"\n  ── M8 Comorbidity Alerts ──")
    for name in patient_names:
        msgs = outputs["M8"].get(name, [])
        if not msgs:
            print(f"  {name.upper():8s}: 0 alerts {'(EXPECTED)' if name == 'priya' else ''}")
            continue
        print(f"  {name.upper():8s}: {len(msgs)} alerts")
        for i, m in enumerate(msgs, 1):
            rule = m.get("ruleId", "?")
            sev = m.get("severity", "?")
            summary = str(m.get("triggerSummary", m.get("description", "")))[:70]
            print(f"    [{i}] {rule} ({sev}) — {summary}")


def print_m12_detail(outputs, patient_names):
    """Print M12 intervention window signal details."""
    print(f"\n  ── M12 Intervention Window Signals ──")
    for name in patient_names:
        msgs = outputs["M12"].get(name, [])
        if not msgs:
            continue
        print(f"  {name.upper():8s}: {len(msgs)} signals")
        for i, m in enumerate(msgs, 1):
            sig = m.get("signalType", m.get("signal_type", "?"))
            intv = m.get("interventionId", m.get("intervention_id", "?"))
            intv_short = intv[-30:] if len(str(intv)) > 30 else intv
            print(f"    [{i}] {sig} — {intv_short}")


def print_m13_detail(outputs, patient_names):
    """Print M13 clinical state change details."""
    print(f"\n  ── M13 Clinical State Changes ──")
    for name in patient_names:
        msgs = outputs["M13"].get(name, [])
        if not msgs:
            print(f"  {name.upper():8s}: 0 state changes")
            continue
        print(f"  {name.upper():8s}: {len(msgs)} state changes")
        for i, m in enumerate(msgs, 1):
            ctype = m.get("change_type", "?")
            priority = m.get("priority", "?")
            vel = m.get("ckm_velocity_at_change", {})
            comp = vel.get("composite_classification", "?")
            conf = m.get("data_completeness_at_change", m.get("confidenceScore", "?"))
            domains = vel.get("domain_velocities", {})
            print(f"    [{i}] {ctype} ({priority}) composite={comp} confidence={conf}")
            if domains:
                parts = [f"{k}={v:.3f}" for k, v in domains.items() if isinstance(v, (int, float))]
                if parts:
                    print(f"        velocities: {', '.join(parts)}")


