def publish(topic, msg):
    """Publish a single JSON message to a Kafka topic via Docker exec."""
    j = json.dumps(msg, separators=(",", ":"))
    r = subprocess.run(
        ["docker", "exec", "-i", KAFKA_CONTAINER, "kafka-console-producer",
         "--bootstrap-server", BOOTSTRAP, "--topic", topic],
        input=j, capture_output=True, text=True, timeout=15)
    return r.returncode == 0


def consume(topic, timeout=20, max_msgs=2000):
    """Consume all messages from a Kafka topic."""
    r = subprocess.run(
        ["docker", "exec", KAFKA_CONTAINER, "kafka-console-consumer",
         "--bootstrap-server", BOOTSTRAP, "--topic", topic,
         "--from-beginning", "--timeout-ms", str(timeout * 1000),
         "--max-messages", str(max_msgs)],
        capture_output=True, text=True, timeout=timeout + 30)
    msgs = []
    for line in r.stdout.strip().split("\n"):
        if not line.strip():
            continue
        try:
            msgs.append(json.loads(line.strip()))
        except json.JSONDecodeError:
            pass
    return msgs


def topic_count(topic):
    """Get total message count for a topic using offset shell."""
    r = subprocess.run(
        ["docker", "exec", KAFKA_CONTAINER, "kafka-run-class",
         "kafka.tools.GetOffsetShell", "--broker-list", "localhost:9092",
         "--topic", topic, "--time", "-1"],
        capture_output=True, text=True, timeout=10)
    total = 0
    for line in r.stdout.strip().split("\n"):
        parts = line.split(":")
        if len(parts) >= 3:
            try:
                total += int(parts[2])
            except ValueError:
                pass
    return total


def get_patient_id(msg):
    """Extract patient ID from a message (handles both field naming conventions)."""
    return msg.get("patient_id") or msg.get("patientId") or ""


def filter_by_patient(msgs, patient_id):
    """Filter messages belonging to a specific patient."""
    return [m for m in msgs if get_patient_id(m) == patient_id]


# ═══════════════════════════════════════════════════════════════════════
#  INJECTION ENGINE
# ═══════════════════════════════════════════════════════════════════════

def inject_day(day_data, inter_event_delay=0.15):
    """Inject all events for a single day into Kafka topics.

    Returns dict of counts: {"vitals": n, "enriched": n, "interventions": n}
    """
    counts = {"vitals": 0, "enriched": 0, "interventions": 0}

    inject = day_data.get("inject", {})

    # Vitals → ingestion.vitals
    for event in inject.get(TOPIC_VITALS, []):
        if publish(TOPIC_VITALS, event):
            counts["vitals"] += 1
        time.sleep(inter_event_delay)

    # Enriched → enriched-patient-events-v1
    for event in inject.get(TOPIC_ENRICHED, []):
        if publish(TOPIC_ENRICHED, event):
            counts["enriched"] += 1
        time.sleep(inter_event_delay)

    # Interventions → clinical.intervention-events
    for event in inject.get(TOPIC_INTERVENTION, []):
        if publish(TOPIC_INTERVENTION, event):
            counts["interventions"] += 1
        time.sleep(inter_event_delay)

    return counts


# ═══════════════════════════════════════════════════════════════════════
#  OUTPUT COLLECTION
# ═══════════════════════════════════════════════════════════════════════

def collect_all_outputs(patient_ids, timeout=15):
    """Consume all output topics and filter per patient.

    Returns: {module: {patient_name: [messages]}}
    """
    results = {}
    for module, topic in OUTPUT_TOPICS.items():
        all_msgs = consume(topic, timeout=timeout)
        per_patient = {}
        for name, pid in patient_ids.items():
            per_patient[name] = filter_by_patient(all_msgs, pid)
        per_patient["_total"] = len(all_msgs)
        results[module] = per_patient
    return results


