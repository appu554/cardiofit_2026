    @Nested
    @DisplayName("Compliance Rate Calculation Tests")
    class ComplianceRateTests {

        @Test
        @DisplayName("Should calculate 100% compliance rate")
        void testPerfectCompliance() {
            measure.setDenominatorCount(100);
            measure.setNumeratorCount(100);
            measure.setExclusionCount(0);
            measure.setExceptionCount(0);

            measure.calculateComplianceRate();

            assertEquals(100.0, measure.getComplianceRate(), 0.001);
        }

        @Test
        @DisplayName("Should calculate 75% compliance rate")
        void testPartialCompliance() {
            measure.setDenominatorCount(100);
            measure.setNumeratorCount(75);
            measure.setExclusionCount(0);
            measure.setExceptionCount(0);

            measure.calculateComplianceRate();

            assertEquals(75.0, measure.getComplianceRate(), 0.001);
        }

        @Test
        @DisplayName("Should adjust denominator for exclusions")
        void testExclusionAdjustment() {
            measure.setDenominatorCount(100);
            measure.setNumeratorCount(80);
            measure.setExclusionCount(10);
            measure.setExceptionCount(0);

            measure.calculateComplianceRate();

            // 80 / (100 - 10) = 88.89%
            assertEquals(88.89, measure.getComplianceRate(), 0.01);
        }

        @Test
        @DisplayName("Should adjust denominator for exceptions")
        void testExceptionAdjustment() {
            measure.setDenominatorCount(100);
            measure.setNumeratorCount(75);
            measure.setExclusionCount(5);
            measure.setExceptionCount(5);

            measure.calculateComplianceRate();

            // 75 / (100 - 5 - 5) = 83.33%
            assertEquals(83.33, measure.getComplianceRate(), 0.01);
        }

        @Test
        @DisplayName("Should return zero for zero adjusted denominator")
        void testZeroAdjustedDenominator() {
            measure.setDenominatorCount(10);
            measure.setNumeratorCount(5);
            measure.setExclusionCount(10);
            measure.setExceptionCount(0);

            measure.calculateComplianceRate();

            assertEquals(0.0, measure.getComplianceRate(), 0.001);
        }
    }

