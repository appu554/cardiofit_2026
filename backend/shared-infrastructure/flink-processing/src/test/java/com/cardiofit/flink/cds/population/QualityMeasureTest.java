    @Nested
    @DisplayName("Quality Measure Creation Tests")
    class QualityMeasureCreationTests {

        @Test
        @DisplayName("Should create measure with required fields")
        void testMeasureCreation() {
            assertNotNull(measure);
            assertEquals(TEST_MEASURE_NAME, measure.getMeasureName());
            assertEquals(TEST_MEASURE_TYPE, measure.getMeasureType());
            assertEquals(TEST_SOURCE, measure.getSource());
            assertNotNull(measure.getLastCalculated());
            assertTrue(measure.isActive());
        }

        @Test
        @DisplayName("Should auto-generate unique measure ID")
        void testAutoGeneratedMeasureId() throws InterruptedException {
            assertNotNull(measure.getMeasureId());
            assertTrue(measure.getMeasureId().startsWith("QM-"));

            Thread.sleep(1); // Ensure different timestamp
            QualityMeasure measure2 = new QualityMeasure("Blood Pressure Control",
                                                         QualityMeasure.MeasureType.OUTCOME,
                                                         QualityMeasure.MeasureSource.CMS);
            assertNotEquals(measure.getMeasureId(), measure2.getMeasureId());
        }

        @Test
        @DisplayName("Should initialize empty criterion lists")
        void testCriterionListsInitialization() {
            assertNotNull(measure.getNumeratorCriteria());
            assertNotNull(measure.getDenominatorCriteria());
            assertNotNull(measure.getExclusionCriteria());
            assertNotNull(measure.getExceptionCriteria());

            assertEquals(0, measure.getNumeratorCriteria().size());
            assertEquals(0, measure.getDenominatorCriteria().size());
            assertEquals(0, measure.getExclusionCriteria().size());
            assertEquals(0, measure.getExceptionCriteria().size());
        }

        @Test
        @DisplayName("Should support all measure types")
        void testAllMeasureTypes() {
            QualityMeasure.MeasureType[] types = QualityMeasure.MeasureType.values();

            assertEquals(6, types.length);
            assertTrue(containsMeasureType(types, QualityMeasure.MeasureType.PROCESS));
            assertTrue(containsMeasureType(types, QualityMeasure.MeasureType.OUTCOME));
            assertTrue(containsMeasureType(types, QualityMeasure.MeasureType.STRUCTURE));
            assertTrue(containsMeasureType(types, QualityMeasure.MeasureType.PATIENT_EXPERIENCE));
            assertTrue(containsMeasureType(types, QualityMeasure.MeasureType.EFFICIENCY));
            assertTrue(containsMeasureType(types, QualityMeasure.MeasureType.COMPOSITE));
        }

        @Test
        @DisplayName("Should support all measure sources")
        void testAllMeasureSources() {
            QualityMeasure.MeasureSource[] sources = QualityMeasure.MeasureSource.values();

            assertEquals(6, sources.length);
            assertTrue(containsMeasureSource(sources, QualityMeasure.MeasureSource.HEDIS));
            assertTrue(containsMeasureSource(sources, QualityMeasure.MeasureSource.CMS));
            assertTrue(containsMeasureSource(sources, QualityMeasure.MeasureSource.NQF));
            assertTrue(containsMeasureSource(sources, QualityMeasure.MeasureSource.TJC));
            assertTrue(containsMeasureSource(sources, QualityMeasure.MeasureSource.NCQA));
            assertTrue(containsMeasureSource(sources, QualityMeasure.MeasureSource.CUSTOM));
        }

        @Test
        @DisplayName("Should support all performance levels")
        void testAllPerformanceLevels() {
            QualityMeasure.PerformanceLevel[] levels = QualityMeasure.PerformanceLevel.values();

            assertEquals(4, levels.length);
            assertTrue(containsPerformanceLevel(levels, QualityMeasure.PerformanceLevel.NEEDS_IMPROVEMENT));
            assertTrue(containsPerformanceLevel(levels, QualityMeasure.PerformanceLevel.MEETS_TARGET));
            assertTrue(containsPerformanceLevel(levels, QualityMeasure.PerformanceLevel.EXCEEDS_TARGET));
            assertTrue(containsPerformanceLevel(levels, QualityMeasure.PerformanceLevel.TOP_DECILE));

            // Test rank ordering
            assertEquals(1, QualityMeasure.PerformanceLevel.NEEDS_IMPROVEMENT.getRank());
            assertEquals(4, QualityMeasure.PerformanceLevel.TOP_DECILE.getRank());

            // Test colors
            assertEquals("red", QualityMeasure.PerformanceLevel.NEEDS_IMPROVEMENT.getColor());
            assertEquals("blue", QualityMeasure.PerformanceLevel.TOP_DECILE.getColor());
        }

        private boolean containsMeasureType(QualityMeasure.MeasureType[] types, QualityMeasure.MeasureType target) {
            for (QualityMeasure.MeasureType type : types) {
                if (type == target) return true;
            }
            return false;
        }

        private boolean containsMeasureSource(QualityMeasure.MeasureSource[] sources, QualityMeasure.MeasureSource target) {
            for (QualityMeasure.MeasureSource source : sources) {
                if (source == target) return true;
            }
            return false;
        }

        private boolean containsPerformanceLevel(QualityMeasure.PerformanceLevel[] levels, QualityMeasure.PerformanceLevel target) {
            for (QualityMeasure.PerformanceLevel level : levels) {
                if (level == target) return true;
            }
            return false;
        }
    }

    @Nested
    @DisplayName("Measure Criterion Tests")
    class MeasureCriterionTests {

        @Test
        @DisplayName("Should create measure criterion")
        void testCriterionCreation() {
            QualityMeasure.MeasureCriterion criterion = new QualityMeasure.MeasureCriterion(
                QualityMeasure.MeasureCriterion.CriterionType.LAB_VALUE,
                "LOINC"
            );

            criterion.setDescription("HbA1c > 9%");
            criterion.setOperator(">");
            criterion.setValue(9.0);
            criterion.addCode("4548-4"); // LOINC HbA1c

            assertEquals(QualityMeasure.MeasureCriterion.CriterionType.LAB_VALUE, criterion.getCriterionType());
            assertEquals("LOINC", criterion.getCodeSystem());
            assertEquals("HbA1c > 9%", criterion.getDescription());
            assertEquals(">", criterion.getOperator());
            assertEquals(9.0, criterion.getValue());
            assertEquals(1, criterion.getCodes().size());
            assertTrue(criterion.getCodes().contains("4548-4"));
        }

        @Test
        @DisplayName("Should support all criterion types")
        void testAllCriterionTypes() {
            QualityMeasure.MeasureCriterion.CriterionType[] types = QualityMeasure.MeasureCriterion.CriterionType.values();

            assertEquals(10, types.length);
            assertTrue(containsCriterionType(types, QualityMeasure.MeasureCriterion.CriterionType.DIAGNOSIS));
            assertTrue(containsCriterionType(types, QualityMeasure.MeasureCriterion.CriterionType.PROCEDURE));
            assertTrue(containsCriterionType(types, QualityMeasure.MeasureCriterion.CriterionType.LAB_VALUE));
            assertTrue(containsCriterionType(types, QualityMeasure.MeasureCriterion.CriterionType.MEDICATION));
            assertTrue(containsCriterionType(types, QualityMeasure.MeasureCriterion.CriterionType.VITAL_SIGN));
            assertTrue(containsCriterionType(types, QualityMeasure.MeasureCriterion.CriterionType.AGE_RANGE));
            assertTrue(containsCriterionType(types, QualityMeasure.MeasureCriterion.CriterionType.GENDER));
            assertTrue(containsCriterionType(types, QualityMeasure.MeasureCriterion.CriterionType.ENCOUNTER_TYPE));
            assertTrue(containsCriterionType(types, QualityMeasure.MeasureCriterion.CriterionType.OBSERVATION));
            assertTrue(containsCriterionType(types, QualityMeasure.MeasureCriterion.CriterionType.CUSTOM));
        }

        @Test
        @DisplayName("Should support BETWEEN operator with second value")
        void testBetweenOperator() {
            QualityMeasure.MeasureCriterion criterion = new QualityMeasure.MeasureCriterion();
            criterion.setCriterionType(QualityMeasure.MeasureCriterion.CriterionType.AGE_RANGE);
            criterion.setDescription("Age between 50 and 75");
            criterion.setOperator("BETWEEN");
            criterion.setValue(50);
            criterion.setSecondValue(75);

            assertEquals(QualityMeasure.MeasureCriterion.CriterionType.AGE_RANGE, criterion.getCriterionType());
            assertEquals("BETWEEN", criterion.getOperator());
            assertEquals(50, criterion.getValue());
            assertEquals(75, criterion.getSecondValue());
        }

        @Test
        @DisplayName("Should set timeframe for criterion")
        void testCriterionTimeframe() {
            QualityMeasure.MeasureCriterion criterion = new QualityMeasure.MeasureCriterion();
            criterion.setTimeframe("past 12 months");

            assertEquals("past 12 months", criterion.getTimeframe());
        }

        private boolean containsCriterionType(QualityMeasure.MeasureCriterion.CriterionType[] types,
                                              QualityMeasure.MeasureCriterion.CriterionType target) {
            for (QualityMeasure.MeasureCriterion.CriterionType type : types) {
                if (type == target) return true;
            }
            return false;
        }
    }

