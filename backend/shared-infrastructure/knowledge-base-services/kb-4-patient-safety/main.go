// Helper functions
func extractStringVariable(variables map[string]interface{}, key string) string {
	if val, ok := variables[key].(string); ok {
		return val
	}
	return ""
}

func convertAlertToGraphQL(alert SafetyAlert) map[string]interface{} {
	gqlAlert := map[string]interface{}{
		"alertId":           alert.AlertID,
		"patientId":         alert.PatientID,
		"drugCode":          alert.DrugCode,
		"ruleId":            alert.RuleID,
		"severity":          alert.Severity,
		"action":            alert.Action,
		"message":           alert.Message,
		"kb1RuleRefs":       alert.KB1RuleRefs,
		"kb3EvidenceRefs":   alert.KB3EvidenceRefs,
		"patientContext":    alert.PatientContext,
		"overrideStatus":    alert.OverrideStatus,
		"overrideReason":    alert.OverrideReason,
		"overrideBy":        alert.OverrideBy,
		"createdBy":         alert.CreatedBy,
		"createdAt":         alert.CreatedAt.Format(time.RFC3339),
		"updatedAt":         alert.UpdatedAt.Format(time.RFC3339),
	}

	if alert.OverrideTime != nil {
		gqlAlert["overrideTimestamp"] = alert.OverrideTime.Format(time.RFC3339)
	}

	return gqlAlert
}

func calculateRiskScore(alerts []SafetyAlert) float64 {
	if len(alerts) == 0 {
		return 0.0
	}

	score := 0.0
	for _, alert := range alerts {
		switch alert.Severity {
		case "CRITICAL":
			score += 10.0
		case "WARN":
			score += 5.0
		case "INFO":
			score += 1.0
		}
	}

	// Normalize to 0-100 scale
	return score / float64(len(alerts)) * 10
}

