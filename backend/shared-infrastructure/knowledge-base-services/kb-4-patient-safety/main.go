func main() {
	log.Printf("🚀 Starting %s v%s...", serviceName, serviceVersion)

	// Initialize safety checker with governed knowledge if available
	knowledgePath := os.Getenv("KNOWLEDGE_PATH")
	jurisdictionStr := os.Getenv("JURISDICTION")
	useGoverned := os.Getenv("USE_GOVERNED_KNOWLEDGE") == "true"

	if useGoverned && knowledgePath != "" {
		// Map jurisdiction string to type
		jurisdiction := safety.JurisdictionGlobal
		switch strings.ToUpper(jurisdictionStr) {
		case "US":
			jurisdiction = safety.JurisdictionUS
		case "AU":
			jurisdiction = safety.JurisdictionAU
		case "IN":
			jurisdiction = safety.JurisdictionIN
		}

		log.Printf("📚 Loading governed knowledge from: %s (jurisdiction: %s)", knowledgePath, jurisdiction)
		var err error
		safetyChecker, err = safety.NewJurisdictionAwareSafetyChecker(knowledgePath, jurisdiction)
		if err != nil {
			log.Printf("⚠️  Failed to load governed knowledge: %v (falling back to built-in)", err)
			safetyChecker = safety.NewSafetyChecker()
			log.Println("✅ Safety checker initialized with built-in drug database")
		} else {
			stats := safetyChecker.GetKnowledgeStats()
			log.Printf("✅ Safety checker initialized with governed knowledge (%d total entries)", stats["total_entries"])
			log.Printf("   📋 BlackBox: %d | HighAlert: %d | Beers: %d | Pregnancy: %d | Lactation: %d",
				stats["blackbox_warnings"], stats["high_alert_medications"], stats["beers_entries"],
				stats["pregnancy_safety"], stats["lactation_safety"])
			log.Printf("   📋 Labs: %d | ACB: %d | Contraindications: %d",
				stats["lab_requirements"], stats["anticholinergic_burdens"], stats["contraindications"])
		}
	} else {
		safetyChecker = safety.NewSafetyChecker()
		log.Println("✅ Safety checker initialized with built-in drug database")
	}

	// Initialize analytics components
	signalDetector = analytics.NewSignalDetector()
	trendAnalyzer = analytics.NewTrendAnalyzer()
	log.Println("✅ Analytics engine initialized (Signal Detection + Trend Analysis)")

	// Initialize explainability layer
	alertExplainer = explainability.NewExplainer(safetyChecker)
	log.Println("✅ Explainability layer initialized (Clinical Decision Explanations)")

	// Initialize database
	db, err := initDatabase()
	if err != nil {
		log.Fatalf("❌ Failed to initialize database: %v", err)
	}
	log.Println("✅ Database connected successfully")

	// Auto-migrate models — skip if table already exists from SQL migrations
	if !db.Migrator().HasTable(&SafetyAlert{}) {
		if err := db.AutoMigrate(&SafetyAlert{}); err != nil {
			log.Printf("⚠️  Auto-migration warning: %v", err)
		}
	} else {
		log.Println("ℹ️  safety_alerts table exists (from SQL migrations), skipping GORM AutoMigrate")
	}

	// Initialize Gin router
	gin.SetMode(gin.ReleaseMode)
	router := gin.New()
	router.Use(gin.Logger())
	router.Use(gin.Recovery())

	// Add CORS middleware
	router.Use(func(c *gin.Context) {
		c.Header("Access-Control-Allow-Origin", "*")
		c.Header("Access-Control-Allow-Methods", "GET, POST, PUT, DELETE, OPTIONS")
		c.Header("Access-Control-Allow-Headers", "Content-Type, Authorization, Upgrade, Connection")

		if c.Request.Method == "OPTIONS" {
			c.AbortWithStatus(204)
			return
		}

		c.Next()
	})

	// Setup routes
	setupRoutes(router, db)

	// Setup GraphQL Federation endpoints
	setupGraphQLFederation(router, db)

	// Initialize GraphQL Subscription Manager
	subscriptionManager = graphql.NewSubscriptionManager(signalDetector, trendAnalyzer)
	subscriptionManager.RegisterGraphQLRoutes(router)
	log.Println("✅ GraphQL Subscriptions initialized (WebSocket support)")

	// Start gRPC server (Phase 5)
	grpcPort := os.Getenv("GRPC_PORT")
	if grpcPort == "" {
		grpcPort = "50051" // Default gRPC port
	}
	grpcPortInt, _ := strconv.Atoi(grpcPort)
	go func() {
		grpcServer, err := grpcserver.StartGRPCServer(grpcPortInt, safetyChecker)
		if err != nil {
			log.Printf("⚠️  gRPC server failed to start: %v", err)
		} else {
			log.Printf("✅ gRPC server started on port %s", grpcPort)
			_ = grpcServer // Keep reference
		}
	}()

	// Start HTTP server
	port := os.Getenv("PORT")
	if port == "" {
		port = "8088" // Default port for KB-4 Patient Safety (per CLAUDE.md)
	}

	log.Printf("🌐 HTTP Server starting on port %s", port)
	log.Printf("🔌 gRPC Server starting on port %s", grpcPort)
	log.Printf("📋 Available endpoints:")
	log.Printf("   GET  /health - Health check")
	log.Printf("   GET  /ready - Readiness check")
	log.Printf("   GET  /v1/alerts - Get safety alerts")
	log.Printf("   POST /v1/alerts - Create safety alert")
	log.Printf("   POST /v1/check - Safety check")
	log.Printf("   GET  /v1/stats - Service statistics")
	log.Printf("   GET  /api/federation - GraphQL Federation schema")
	log.Printf("   POST /api/federation - GraphQL Federation query")
	log.Printf("   GET  /graphql/subscriptions - WebSocket subscriptions")
	log.Printf("   POST /v1/analytics/signals - Signal detection")
	log.Printf("   POST /v1/analytics/trends - Trend analysis")
	log.Printf("   GET  /v1/analytics/dashboard - Safety dashboard")

	if err := http.ListenAndServe(":"+port, router); err != nil {
		log.Fatalf("❌ Server failed to start: %v", err)
	}
}

func initDatabase() (*gorm.DB, error) {
	dsn := os.Getenv("DATABASE_URL")
	if dsn == "" {
		dsn = "postgres://kb4_safety_user:kb4_safety_password@localhost:5436/kb4_patient_safety?sslmode=disable"
	}

	db, err := gorm.Open(postgres.Open(dsn), &gorm.Config{})
	if err != nil {
		return nil, err
	}

	// Test connection
	sqlDB, err := db.DB()
	if err != nil {
		return nil, err
	}

	if err := sqlDB.Ping(); err != nil {
		return nil, err
	}

	return db, nil
}

