func getSafetyAlerts(db *gorm.DB) gin.HandlerFunc {
	return func(c *gin.Context) {
		patientID := c.Query("patient_id")
		severity := c.Query("severity")
		active := c.Query("active")

		var alerts []SafetyAlert
		query := db.Model(&SafetyAlert{})

		if patientID != "" {
			query = query.Where("patient_id = ?", patientID)
		}
		if severity != "" {
			query = query.Where("severity = ?", severity)
		}
		if active != "" {
			if active == "true" {
				query = query.Where("override_status = ?", "active")
			}
		}

		if err := query.Order("created_at DESC").Find(&alerts).Error; err != nil {
			c.JSON(500, gin.H{
				"success": false,
				"error":   "Failed to retrieve safety alerts",
				"details": err.Error(),
			})
			return
		}

		c.JSON(200, gin.H{
			"success": true,
			"alerts":  alerts,
			"count":   len(alerts),
		})
	}
}

func createSafetyAlert(db *gorm.DB) gin.HandlerFunc {
	return func(c *gin.Context) {
		var request struct {
			PatientID       string   `json:"patient_id" binding:"required"`
			DrugCode        string   `json:"drug_code" binding:"required"`
			RuleID          string   `json:"rule_id" binding:"required"`
			Severity        string   `json:"severity" binding:"required"`
			Action          string   `json:"action" binding:"required"`
			Message         string   `json:"message" binding:"required"`
			KB1RuleRefs     []string `json:"kb1_rule_refs"`
			PatientContext  string   `json:"patient_context"`
		}

		if err := c.ShouldBindJSON(&request); err != nil {
			c.JSON(400, gin.H{
				"success": false,
				"error":   "Invalid request format",
				"details": err.Error(),
			})
			return
		}

		alert := &SafetyAlert{
			AlertID:         fmt.Sprintf("KB4-%d", time.Now().Unix()),
			PatientID:       request.PatientID,
			DrugCode:        request.DrugCode,
			RuleID:          request.RuleID,
			Severity:        request.Severity,
			Action:          request.Action,
			Message:         request.Message,
			KB1RuleRefs:     request.KB1RuleRefs,
			PatientContext:  request.PatientContext,
			OverrideStatus:  "active",
			CreatedBy:       "kb4-safety-engine",
		}

		if err := db.Create(alert).Error; err != nil {
			c.JSON(500, gin.H{
				"success": false,
				"error":   "Failed to create safety alert",
				"details": err.Error(),
			})
			return
		}

		log.Printf("✅ Safety alert created: %s for patient %s", alert.AlertID, alert.PatientID)

		c.JSON(200, gin.H{
			"success":  true,
			"alert_id": alert.AlertID,
			"message":  "Safety alert created successfully",
			"alert":    alert,
		})
	}
}

func overrideSafetyAlert(db *gorm.DB) gin.HandlerFunc {
	return func(c *gin.Context) {
		alertID := c.Param("alert_id")

		var request struct {
			OverrideReason string `json:"override_reason" binding:"required"`
			OverrideBy     string `json:"override_by" binding:"required"`
		}

		if err := c.ShouldBindJSON(&request); err != nil {
			c.JSON(400, gin.H{
				"success": false,
				"error":   "Invalid request format",
				"details": err.Error(),
			})
			return
		}

		var alert SafetyAlert
		if err := db.Where("alert_id = ?", alertID).First(&alert).Error; err != nil {
			c.JSON(404, gin.H{
				"success": false,
				"error":   "Safety alert not found",
				"alert_id": alertID,
			})
			return
		}

		now := time.Now()
		alert.OverrideStatus = "overridden"
		alert.OverrideReason = request.OverrideReason
		alert.OverrideBy = request.OverrideBy
		alert.OverrideTime = &now

		if err := db.Save(&alert).Error; err != nil {
			c.JSON(500, gin.H{
				"success": false,
				"error":   "Failed to override safety alert",
				"details": err.Error(),
			})
			return
		}

		log.Printf("✅ Safety alert overridden: %s by %s", alert.AlertID, request.OverrideBy)

		c.JSON(200, gin.H{
			"success": true,
			"message": "Safety alert overridden successfully",
			"alert":   alert,
		})
	}
}

