// handleSearchBlackBoxWarnings searches black box warnings by risk category
func handleSearchBlackBoxWarnings() gin.HandlerFunc {
	return func(c *gin.Context) {
		category := c.Query("category")
		hasRems := c.Query("hasRems")

		db := safety.GetDatabase()
		warnings := db.GetAllBlackBoxWarnings()

		var filtered []*safety.BlackBoxWarning
		for _, w := range warnings {
			// Filter by category
			if category != "" {
				found := false
				for _, cat := range w.RiskCategories {
					if strings.Contains(strings.ToLower(cat), strings.ToLower(category)) {
						found = true
						break
					}
				}
				if !found {
					continue
				}
			}

			// Filter by REMS requirement
			if hasRems != "" {
				wantsRems := hasRems == "true" || hasRems == "1"
				if w.HasREMS != wantsRems {
					continue
				}
			}

			filtered = append(filtered, w)
		}

		c.JSON(http.StatusOK, gin.H{
			"count":    len(filtered),
			"filters":  gin.H{"category": category, "hasRems": hasRems},
			"warnings": filtered,
		})
	}
}

