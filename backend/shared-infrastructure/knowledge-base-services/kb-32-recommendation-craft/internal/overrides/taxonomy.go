// Package overrides implements the override-reason taxonomy for clinical-safety
// audit capture.
//
// VisibilityClass: AD — override capture for clinical-safety audit per Guidelines §5
//
// The 20 reason codes are split into two groups:
//   - 12 Wright/McCoy foundation codes: alert_fatigue, irrelevant_to_patient,
//     patient_preference, clinical_judgment, alternative_pursued,
//     monitoring_in_place, low_priority, documentation_concern,
//     uncertain_evidence, system_error, workflow_constraint, duplicative_alert.
//   - 8 ACOP extension codes: goals_of_care_aligned, deprescribing_underway,
//     frailty_consideration, family_consensus_pending, sdm_review_required,
//     trial_period_active, audit_visit_imminent, cross_resident_pattern.
//
// Reason codes mirror the CHECK constraint that will be emitted by migration 042.
// AppropriatenessFlag values mirror the CHECK constraint in the same migration.
package overrides

import (
	"errors"
	"time"
)

// ---------------------------------------------------------------------------
// Sentinel errors
// ---------------------------------------------------------------------------

// ErrInvalidReasonCode is returned when an OverrideReason carries a ReasonCode
// that is not in the canonical set of 20 codes defined by the Recommendation
// Craft Guidelines Part 5. The check is case-sensitive.
var ErrInvalidReasonCode = errors.New("overrides: invalid reason code")

// ErrInvalidFlag is returned when an OverrideReason carries an
// AppropriatenessFlag that is not one of the three accepted values:
// "appropriate_override", "inappropriate_override", "mixed".
var ErrInvalidFlag = errors.New("overrides: invalid appropriateness flag")

// ErrEmptyReasoning is returned when an OverrideReason has an empty Reasoning
// field. Free-text rationale is mandatory for every captured override.
var ErrEmptyReasoning = errors.New("overrides: reasoning must not be empty")

// ---------------------------------------------------------------------------
// Canonical code sets
// ---------------------------------------------------------------------------

